        private static void AssignDbTrackFieldValue(Dictionary<string, object> dictOneToOneFields)
        {
            if (dictOneToOneFields.ContainsKey(EmSystemDbTrackField.AppCreatedByID.ToString()))
            {
                int? createdById = ControlTypeValueConverter.ConvertValueToInt(dictOneToOneFields[EmSystemDbTrackField.AppCreatedByID.ToString()]);
                if (!createdById.HasValue || createdById.Value == 0)
                {
                    dictOneToOneFields[EmSystemDbTrackField.AppCreatedByID.ToString()] = ServerContext.Instance.CurrentUid;
                }

            }
            if (dictOneToOneFields.ContainsKey(EmSystemDbTrackField.AppCreatedDate.ToString()))
            {
                if (dictOneToOneFields[EmSystemDbTrackField.AppCreatedDate.ToString()] == null)
                {
                    dictOneToOneFields[EmSystemDbTrackField.AppCreatedDate.ToString()] = System.DateTime.UtcNow;

                }


            }

            if (dictOneToOneFields.ContainsKey(EmSystemDbTrackField.AppCreatedByCompanyID.ToString()))
            {
                if (dictOneToOneFields[EmSystemDbTrackField.AppCreatedByCompanyID.ToString()] == null)
                {
                    dictOneToOneFields[EmSystemDbTrackField.AppCreatedByCompanyID.ToString()] = ServerContext.Instance.CurrentCompanyId;

                }


            }


            if (dictOneToOneFields.ContainsKey(EmSystemDbTrackField.AppModifiedByID.ToString()))
            {
                dictOneToOneFields[EmSystemDbTrackField.AppModifiedByID.ToString()] = ServerContext.Instance.CurrentUid;
            }
            if (dictOneToOneFields.ContainsKey(EmSystemDbTrackField.AppModifiedDate.ToString()))
            {
                dictOneToOneFields[EmSystemDbTrackField.AppModifiedDate.ToString()] = System.DateTime.UtcNow;
            }
        }

        private static void AsignSystemBuilttokenToDbMappingField(Dictionary<string, object> dictOneToOneFields, AppTransactionUnitExDto transactionUnitExDto, string dbField)
        {
            EmBLFiledMappingSystemTokenField emtoken = (EmBLFiledMappingSystemTokenField)transactionUnitExDto.DictDbFieldSysToken[dbField];
            var transFieldDto = transactionUnitExDto.AppTransactionFieldList.FirstOrDefault(o => o.DataBaseFieldName == dbField);

            if (dbField == EmBLFiledMappingSystemTokenField.TransactionID.ToString() && dictOneToOneFields.ContainsKey(EmBLFiledMappingSystemTokenField.TransactionID.ToString()))
            {
                dictOneToOneFields[EmBLFiledMappingSystemTokenField.TransactionID.ToString()] = transactionUnitExDto.TransactionId;
            }
            else if (emtoken == EmBLFiledMappingSystemTokenField.TransactionID)
            {
                dictOneToOneFields[dbField] = transactionUnitExDto.TransactionId;
            }
            else
            {
                object orgInputValeu = dictOneToOneFields[dbField];

                dictOneToOneFields[dbField] = GetSystemTokenValue(orgInputValeu, emtoken);

            }
      

          
        }

        internal static object GetSystemTokenValue( object orgInputValue, EmBLFiledMappingSystemTokenField emtoken)
        {
            object toReturn = null;

            if (emtoken == EmBLFiledMappingSystemTokenField.CurrentUserID)
            {
               toReturn = AppSecurityUserBL.CurrentUserEntity.UserId;

            }           
            else if (emtoken == EmBLFiledMappingSystemTokenField.CurrentDatetime)
            {
               toReturn = System.DateTime.UtcNow; ;
            }





            else if (emtoken == EmBLFiledMappingSystemTokenField.DefaultNewGuid)
            {
                if (orgInputValue == null)
                {
                   toReturn = System.Guid.NewGuid();

                }

            }

        

            else if (emtoken == EmBLFiledMappingSystemTokenField.LastModifiedByCustomerID)
            {
                if (AppSecurityUserBL.CurrentUserEntity.DomainId == (int)EmAppUserType.Customer)
                {
                    int? partnerId = ServerContext.Instance.CurrnetClientIdentity.CurrentPartnerId;
                   toReturn = partnerId;
                }
            }
            else if (emtoken == EmBLFiledMappingSystemTokenField.LastModifiedBySupplierID)
            {
                if (AppSecurityUserBL.CurrentUserEntity.DomainId == (int)EmAppUserType.Supplier)
                {
                    int? partnerId = ServerContext.Instance.CurrnetClientIdentity.CurrentPartnerId;
                   toReturn = partnerId;
                }
            }
            else if (emtoken == EmBLFiledMappingSystemTokenField.LastModifiedByClientAgentID)
            {
                if (AppSecurityUserBL.CurrentUserEntity.DomainId == (int)EmAppUserType.ClientAgent)
                {
                    int? partnerId = ServerContext.Instance.CurrnetClientIdentity.CurrentPartnerId;
                   toReturn = partnerId;
                }
            }
            else if (emtoken == EmBLFiledMappingSystemTokenField.LastModifiedBySupplierAgentID)
            {
                if (AppSecurityUserBL.CurrentUserEntity.DomainId == (int)EmAppUserType.SupplierAgent)
                {
                    int? partnerId = ServerContext.Instance.CurrnetClientIdentity.CurrentPartnerId;
                   toReturn = partnerId;
                }
            }
            else if (emtoken == EmBLFiledMappingSystemTokenField.CreatedByCustomerID)
            {
                int? orgValue = ControlTypeValueConverter.ConvertValueToInt(orgInputValue);

                if (!orgValue.HasValue)
                {
                    if (AppSecurityUserBL.CurrentUserEntity.DomainId == (int)EmAppUserType.Customer)
                    {
                        int? partnerId = ServerContext.Instance.CurrnetClientIdentity.CurrentPartnerId;
                       toReturn = partnerId;
                    }
                }
            }
            else if (emtoken == EmBLFiledMappingSystemTokenField.CreatedBySupplierID)
            {
                int? orgValue = ControlTypeValueConverter.ConvertValueToInt(orgInputValue);

                if (!orgValue.HasValue)
                {
                    if (AppSecurityUserBL.CurrentUserEntity.DomainId == (int)EmAppUserType.Supplier)
                    {
                        int? partnerId = ServerContext.Instance.CurrnetClientIdentity.CurrentPartnerId;
                       toReturn = partnerId;
                    }
                }
            }
            else if (emtoken == EmBLFiledMappingSystemTokenField.CreatedByClientAgentID)
            {
                int? orgValue = ControlTypeValueConverter.ConvertValueToInt(orgInputValue);

                if (!orgValue.HasValue)
                {
                    if (AppSecurityUserBL.CurrentUserEntity.DomainId == (int)EmAppUserType.ClientAgent)
                    {
                        int? partnerId = ServerContext.Instance.CurrnetClientIdentity.CurrentPartnerId;
                       toReturn = partnerId;
                    }
                }
            }
            else if (emtoken == EmBLFiledMappingSystemTokenField.CreatedBySupplierAgentID)
            {
                int? orgValue = ControlTypeValueConverter.ConvertValueToInt(orgInputValue);

                if (!orgValue.HasValue)
                {
                    if (AppSecurityUserBL.CurrentUserEntity.DomainId == (int)EmAppUserType.SupplierAgent)
                    {
                        int? partnerId = ServerContext.Instance.CurrnetClientIdentity.CurrentPartnerId;
                       toReturn = partnerId;
                    }
                }
            }
            else if (emtoken == EmBLFiledMappingSystemTokenField.UploadFileByTransactionCommand)
            {
                toReturn = orgInputValue;
            }

            return toReturn;
        }


