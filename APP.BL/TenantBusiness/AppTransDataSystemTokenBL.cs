        internal static void AssignAuntoGenerationCodeToUnitField(Dictionary<string, object> dictOneToOneFields, AppTransactionUnitExDto transactionUnitExDto, bool isCallFromSave)
        {
            foreach (var transFieldExDto in transactionUnitExDto.AppTransactionFieldList)
            {
                if (transFieldExDto.AutoIncrementSeed.HasValue 
                    && !string.IsNullOrEmpty(transFieldExDto.DataBaseFieldName) 
                    && dictOneToOneFields.ContainsKey(transFieldExDto.DataBaseFieldName))
                {
                    string autoGeneratedCode = GenerateTransactionFieldAutoIncrment(transFieldExDto, isCallFromSave);
                    dictOneToOneFields[transFieldExDto.DataBaseFieldName] = autoGeneratedCode;
                }
            }
        }

        // when read, increse
        static object LockIncrment = new object();
        private static string GenerateTransactionFieldAutoIncrment(AppTransactionFieldExDto transFieldExDto, bool isCallFromSave)
        {
            string result = string.Empty;

            lock (LockIncrment)
            {
                int lastid = -1;
                if (!transFieldExDto.AutoIncrementLastId.HasValue)
                {
                    if (transFieldExDto.AutoIncrementSeed.HasValue)
                    {
                        lastid = transFieldExDto.AutoIncrementSeed.Value;
                    }
                }
                else
                {
                    lastid = transFieldExDto.AutoIncrementLastId.Value;
                }

                if (lastid != -1)
                {
                    lastid++;
                    result = CreateValueWithMissingCharacter(transFieldExDto, lastid);
                    if (isCallFromSave)
                    {
                        transFieldExDto.AutoIncrementLastId = lastid;
                        UpdateTransactionFiledLastId((int)transFieldExDto.Id, lastid);
                    }
                }                
            }

            return result;
        }

        private static void UpdateTransactionFiledLastId(int transFiledId, int lastId)
        {
            using (DataAccessAdapter adapter = AppTenantAdapterBL.GetTenantAdapter())
            {
                AppTransactionFieldEntity entity = new AppTransactionFieldEntity();

                entity.AutoIncrementLastId = lastId;

                adapter.UpdateEntitiesDirectly(entity, new RelationPredicateBucket(AppTransactionFieldFields.TransactionFieldId == transFiledId));
            }

        }

        private static string CreateValueWithMissingCharacter(AppTransactionFieldExDto transFieldExDto, int lastid)
        {
            string result = "";

            int nbChar;

            if (!string.IsNullOrEmpty(transFieldExDto.AutoIncrementPrefix))
            {
                result = transFieldExDto.AutoIncrementPrefix;

                nbChar = transFieldExDto.AutoIncrementPrefix.Count() + lastid.ToString().Count();
            }
            else
            {
                nbChar = lastid.ToString().Count();
            }

            int maxChar = transFieldExDto.MaxCharLegnth.Value;

            int completedChar = maxChar - nbChar;

            for (int i = 0; i < completedChar; i++)
            {
                result += "0";
            }

            result += lastid;

            return result;
        }

